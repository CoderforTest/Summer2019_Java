package Day03_Variables;

public class Escape_Sequences {

	public static void main(String[] args) {

		System.out.println("Hello Cybertek\nBatch 12 students");
		
		System.out.print("Tomorrow is off");
		System.out.println("True");
		
		System.out.println("Tomorrow is off");
		System.out.println("True");

	
	System.out.print("Tomorrow is off \n");
	System.out.print("True\r");
	System.out.println("False\n\n\n\n");
	System.out.println("Class starts tomorrow.");
	
	System.out.println("\\muhtar");
	System.out.println("'Muhtar'");
	
	/*

	  \n (or \r): starts from a new line

	   \t :  horizontal tab

	   \\: prints a back slash

	   \': prints single quote

	   \": prints double quote

	 

	 

	 \: backslash

	 /:front slash

	 */


		System.out.println("Hello Cybertek\n Batch 12 students");
	

		System.out.print("Tomorrow is off \n");

		System.out.print("True\r");

		System.out.println("False\n\n\n");

		

		System.out.println("\tClass starts tomorrow");

		

		System.out.println("\\muhtar");

		System.out.println("\'Java ");
		System.out.println(" \'Java\' ");
		

		System.out.println("Book is called \"Game of Throne\" ");
	
	}
	}